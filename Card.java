public class Card {
    String name;
    int soldiers;

    public static void main(String[] args) {}
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public void setSoldiers(int soldiers) {
        this.soldiers = soldiers;
    }
    public int getSoldiers() {
        return soldiers;
    }

}
